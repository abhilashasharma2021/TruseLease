package com.truelease.Model;

public class CollectionCategoryData {
    private String coll_categoryID;
    private String name;
    private String categoryID;
    private String image;
    private String path;

    public String getColl_categoryID() {
        return coll_categoryID;
    }

    public void setColl_categoryID(String coll_categoryID) {
        this.coll_categoryID = coll_categoryID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCategoryID() {
        return categoryID;
    }

    public void setCategoryID(String categoryID) {
        this.categoryID = categoryID;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
